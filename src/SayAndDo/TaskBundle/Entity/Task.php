<?php

namespace SayAndDo\TaskBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Task
 */
class Task
{
    /**
     * @var integer
     */
    private $id;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }
    /**
     * @var string
     */
    private $title;

    /**
     * @var string
     */
    private $description;

    /**
     * @var string
     */
    private $status;


    /**
     * Set title
     *
     * @param string $title
     * @return Task
     */
    public function setTitle($title)
    {
        $this->title = $title;

        return $this;
    }

    /**
     * Get title
     *
     * @return string 
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * Set description
     *
     * @param string $description
     * @return Task
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string 
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * Set status
     *
     * @param string $status
     * @return Task
     */
    public function setStatus($status)
    {
        $this->status = $status;

        return $this;
    }

    /**
     * Get status
     *
     * @return string 
     */
    public function getStatus()
    {
        return $this->status;
    }
    /**
     * @var \SayAndDo\ProfileBundle\Entity\Profile
     */
    private $profile;


    /**
     * Set profile
     *
     * @param \SayAndDo\ProfileBundle\Entity\Profile $profile
     * @return Task
     */
    public function setProfile(\SayAndDo\ProfileBundle\Entity\Profile $profile = null)
    {
        $this->profile = $profile;

        return $this;
    }

    /**
     * Get profile
     *
     * @return \SayAndDo\ProfileBundle\Entity\Profile 
     */
    public function getProfile()
    {
        return $this->profile;
    }
    /**
     * @var \SayAndDo\PromiseBundle\Entity\Promise
     */
    private $promise;


    /**
     * Set promise
     *
     * @param \SayAndDo\PromiseBundle\Entity\Promise $promise
     * @return Task
     */
    public function setPromise(\SayAndDo\PromiseBundle\Entity\Promise $promise = null)
    {
        $this->promise = $promise;

        return $this;
    }

    /**
     * Get promise
     *
     * @return \SayAndDo\PromiseBundle\Entity\Promise 
     */
    public function getPromise()
    {
        return $this->promise;
    }
}
